/*
TREES DATA STRUCTURE:
Trees are hierarchical data structures with nodes connected by edges.
Root: Top node with no parent
Leaf: Node with no children
Binary Tree: Each node has at most 2 children (left, right)
BST: Binary Search Tree where left < parent < right
Traversal: Inorder (left-root-right), Preorder (root-left-right), Postorder (left-right-root)
Time Complexity: 
    Search/Insert/Delete O(log n) for balanced trees
    O(n) worst case
*/